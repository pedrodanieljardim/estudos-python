# Função Print

print(123456)
# Separação por espaço
print('Belo Horizonte','MG')
# Usando o parâmetro sep da função printf para separar as strings por um Caractere escolhido
print('Contagem','MG',sep = '-')
# outro parâmetro da função printf é o end: que possibilitar colocar algo no final da impressão...
# por padrão, o end é prenchido por \n para quebrar a linha....
print('Contagem','MG',sep = '-',end='#######')
# python é case sensitive logo, Print != print